def set_form_labels(form, labels: dict[str, str] | None = None) -> None:
    merged_labels = {**FORM_LABELS, **(labels or {})}
    for field_name, label in merged_labels.items():
        if field_name in form.fields:
            form.fields[field_name].label = label


def code_label(value: object) -> str:
    if value in [None, ""]:
        return ""
    text = str(value)
    return CODE_LABELS.get(text, text)
